
public class Grades {
   double grade;
   String subject;
public double getGrade() {
	return grade;
}
public void setGrade(int grade) {
	this.grade = grade;
}
public String getSubject() {
	return subject;
}
public void setSubject(String subject) {
	this.subject = subject;
}
public Grades(double grade, String subject) {
	super();
	this.grade = grade;
	this.subject = subject;
}
   
}
